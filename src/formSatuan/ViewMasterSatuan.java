    public void tampilDataList(String cariData) {
        DefaultTableModel listTableSatuan = new DefaultTableModel();
        listTableSatuan.addColumn("No");
        listTableSatuan.addColumn("Kode");
        listTableSatuan.addColumn("Nama Satuan");
        listTableSatuan.addColumn("Status");

        try {
            java.sql.Connection conn = (java.sql.Connection) database.Koneksi.koneksiDB();
            String sql = "{CALL SearchProdukSatuan(?)}";
            try (CallableStatement cst = conn.prepareCall(sql)) {
                cst.setString(1, cariData);

                ResultSet rs = cst.executeQuery();
                while (rs.next()) {
                    listTableSatuan.addRow(new Object[]{
                        rs.getString("satuan_id"),
                        rs.getString("satuan_kode"),
                        rs.getString("satuan_nama"),
                        rs.getString("satuan_status"),
                    });
                }
                satuanGridTable.setModel(new DefaultTableModel());
                satuanGridTable.setModel(listTableSatuan);
 
            }
        } catch (SQLException e) {
            e.printStackTrace();  // or log the exception
        } catch (Exception e) {
            e.printStackTrace();
        }
    
}
