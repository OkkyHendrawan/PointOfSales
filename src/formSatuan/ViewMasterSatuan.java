    public void setFormUpdateMasterSatuan(){
        int selectedRowIndex = satuanGridTable.getSelectedRow();
         
        // A row is selected, retrieve data and perform the update
        String satuan_id = (String) satuanGridTable.getValueAt(selectedRowIndex, 0);
        String satuan_kode = (String) satuanGridTable.getValueAt(selectedRowIndex, 1);
        String satuan_nama = (String) satuanGridTable.getValueAt(selectedRowIndex, 2);
        String satuan_status = (String) satuanGridTable.getValueAt(selectedRowIndex, 3);
        
        Component parentComponent = null; // Replace with the actual component reference
        isianSatuan.setLocationRelativeTo(parentComponent);
        isianSatuan.setDataUpdateFormIsianSatuan(satuan_id,satuan_kode, satuan_nama, satuan_status);
        isianSatuan.setParentisianSatuan(this);
        isianSatuan.setVisible(true);
    }
