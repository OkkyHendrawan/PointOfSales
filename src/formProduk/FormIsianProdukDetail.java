    public void storeSatuanProduk() {
        // Assuming you have a database connection 'conn'

        try {
            java.sql.Connection conn = (java.sql.Connection) database.Koneksi.koneksiDB();
            String sql = "SELECT satuan_id, satuan_kode FROM produk_satuan WHERE satuan_status='Aktif'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            List<String> items = new ArrayList<>();  // Initialize the list

            while (rs.next()) {
                int satuanId = rs.getInt("satuan_id");
                String satuanNama = rs.getString("satuan_kode");

                // Add the display value to the ComboBox
                items.add(satuanNama);

                // Add the association to the Map
                satuanMap.put(satuanNama, satuanId);
            }

            // Populate the ComboBox with the items
            cboSatuanField.setModel(new DefaultComboBoxModel<>(items.toArray(new String[0])));

            // Add a listener to get the selected value and use the Map to get the corresponding ID
            cboSatuanField.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    String selectedSatuanNama = (String) cboSatuanField.getSelectedItem();
                    int selectedSatuanId = satuanMap.get(selectedSatuanNama);
                    // Use the selectedKategoriId as needed
                }
            });

            pst.close();

        } catch (SQLException e) {
            e.printStackTrace();
            // Handle exception appropriately
        }
    }
    
