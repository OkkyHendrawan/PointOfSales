    public void insertRowTableSatuan(){
        // TODO add your handling code here:
        String pdetailSatuanNamaValue = (String) cboSatuanField.getSelectedItem();
        String pdetailHargaValue = satuanHargaField.getText();
        String pdetailNilaiValue = satuanNilaiField.getText();
        String pdetailDefaultValue = (String) satuanDefaultField.getSelectedItem();

        try {
            // Check if the selected value exists in the satuanMap
            if (satuanMap.containsKey(pdetailSatuanNamaValue)) {
                // Get the kategoriId from the map based on the selected kategoriNama
                int pdetailSatuanId = satuanMap.get(pdetailSatuanNamaValue);
                
                // Call the method in the parent FormIsianProduk to add the row
                parentForm.addRowTableSatuan(pdetailSatuanId, pdetailSatuanNamaValue, pdetailHargaValue, pdetailNilaiValue, pdetailDefaultValue);

                // Close the current FormIsianProdukDetail
                this.dispose();

                JOptionPane.showMessageDialog(this, "Produk Detail Berhasil Disimpan!");
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Satuan not found in the map: " + pdetailSatuanNamaValue);
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Produk Gagal Disimpan!");
            // handle exceptions, at least log them
            e.printStackTrace();
        }
        // Handle the exception
        // or log the exception
    }
    
